package com.searshc.hs.agreement.agreementservice.domain;

public class AgreementParameterVO {
	
	private int customerNumber;
	private int agreementSuffix;
	private String dbSynonym;
	
	public int getCustomerNumber() {
		return customerNumber;
	}
	public void setCustomerNumber(int customerNumber) {
		this.customerNumber = customerNumber;
	}
	public int getAgreementSuffix() {
		return agreementSuffix;
	}
	public void setAgreementSuffix(int agreementSuffix) {
		this.agreementSuffix = agreementSuffix;
	}
	public String getDbSynonym() {
		return dbSynonym;
	}
	public void setDbSynonym(String dbSynonym) {
		this.dbSynonym = dbSynonym;
	}
	
	

}
